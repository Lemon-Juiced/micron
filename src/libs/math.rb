# Micron's Math Library

class Math
    def self.add(x, y)
      return x + y
    end
    def self.sub(x, y)
      return x - y
    end
    def self.mul(x, y)
      return x * y
    end
    def self.div(x, y)
      return x / y
    end
    def self.mod(x, y)
      return x % y
    end
    def self.exp(x, y)
      return x ** y
    end
    def self.rad(x, y)
      return x ** (1.0 / y)
    end
end